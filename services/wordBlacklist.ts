export const normalizeBlacklistWord = (value: string): string =>
  value.trim().toUpperCase().replace(/[^A-Z]/g, '');

export const isBlacklistedWord = (word: string | null | undefined): boolean => {
  if (!word) return false;
  return WORD_BLACKLIST.has(normalizeBlacklistWord(word));
};
